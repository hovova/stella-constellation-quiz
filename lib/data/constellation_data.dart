    name: name,
    description: description,
    difficulty: difficulty,
    hemisphere: hemisphere,
    bestSeason: bestSeason,
    brightestStar: brightestStar,
    mythology: mythology,
    imagePath: 'assets/constellations/$id.png',
    iconPath: 'assets/constellation_icons/$id.png',
    localizedNames: {
      'en': name,
      'uk': _ukNames[id] ?? name,
